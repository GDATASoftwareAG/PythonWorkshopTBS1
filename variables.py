# integer
x = 12345
print(x)

# double
x = 3.1415
print(x)

# string
x = 'hello'
print(x)
# or (same)
x = "hello"
print(x)

# list
x = [0, 1, 2]
print(x)

# tuple
x = (0, 1, 2)
print(x)

# file
x = open('variables.py', 'r')
print(x)
x.close()

# Variables a, b and c have been assigned with the same value (1)
a = b = c = 1
print(a)
print(b)
print(c)

# When you change one of these only the one changes
a = 2
print(a)
print(b)
print(c)
